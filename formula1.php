                    <h3>2010-2014</h3>
                        <p>The team was rebranded as Mercedes GP Petronas Formula One Team, and Mercedes would now compete in the constructors' championship for the first time. 
                        Ross Brawn continued his duties as team principal and the team retained its base and workforce in Brackley, close to the Mercedes-Benz Formula One 
                        engine plant (formerly Ilmor Engineering) in Brixworth.</p>
                        <p>The team has a <em>complex</em> history: Its entry can be traced back to Tyrrell Racing, who competed as a constructor from 1970 until 1998, until being 
                        bought by British American Tobacco at the end of 1997 so its entry could be transferred to the then new constructor British American Racing (BAR) 
                        in 1999. BAR, who had formed a partnership with Honda, eventually became Honda Racing F1 Team in 2006 when British American Tobacco withdrew from the sport. </p>
                        <p>It again changed hands in 2008, when Honda withdrew, and was purchased by the team's management, naming it Brawn GP after team principal Ross Brawn. 
                        Brawn used engines from Mercedes-Benz High Performance Engines, and despite running on a low budget, Jenson Button won six of the first seven races 
                        and ultimately the 2009 Drivers' Championship, while Brawn won the Constructors' Championship. <strong>It was the first time in the sport's sixty-year history
                        that a team won both titles in its maiden season.</strong></p>
                        <p>Nico Rosberg scored Mercedes's first podium finish as a works team since 1955 at the 2010 Malaysian Grand Prix Team Mercedes GP hired German drivers Nico Rosberg, 
                        and seven-time world champion Michael Schumacher, who returned to Formula One after a three-year absence, and Nick Heidfeld as the test and reserve driver. 
                        Of Brawn's 2009 drivers, Jenson Button signed for McLaren, whilst Rubens Barrichello moved to Rosberg's former seat with Williams team for 2010.</p>
                        <p>For 2012, the team removed the GP from their name and added the name of AMG, the high performance brand of Daimler AG, to their title. The team would  
                        officially get the designation Mercedes AMG Petronas F1 Team. At the start of the season Mercedes was the subject of protest over the use of a "radical" 
                        rear wing concept on the Mercedes F1 W03 racing car, which was not settled until the third race in China when <strong>the stewards unanimously rejected the protest.</strong></p>
                        <p>On 28 September 2012, it was announced that McLaren driver Lewis Hamilton would join the Mercedes team from the 2013 season onwards, having signed a three-year deal to partner 
                        Nico Rosberg in the team. In January 2013, Toto Wolff became an executive director of the Mercedes AMG Petronas Formula One Team, with his business partner Rene Berger
                        becoming non-executive director. In addition to joining the team as managing partner, he also acquired 30% of Mercedes-Benz Grand Prix Ltd, with a further 10% held by 
                        Niki Lauda, chairman of the board, and 60% by the parent company. Wolff took over the co-ordination of all Mercedes-Benz motorsport 
                        activities, a responsibility previously held by Norbert Haug.</p>
                        <p>Mercedes then went on to take third place in the Canadian Grand Prix courtesy of Hamilton, followed by another win for Rosberg at the British Grand Prix after the 
                        team took 1–2 in qualifying. The team then celebrated their third win of the season after Hamilton took his first victory for the team at the Hungarian Grand Prix, 
                        resulting in second place ahead of Ferrari in the Constructors' Championship.</p>

                <p>Torger Christian "Toto" Wolff (born 12 January 1972) is an Austrian billionaire motorsport executive, investor, and former racing driver.
                 He holds a 33% stake in the Mercedes-AMG Petronas F1 Team and is Team Principal and CEO of the team.</p>

                <p>Wolff began his motorsport career in the Austrian Formula Ford Championship and the German Formula Ford Series. He won his category in the 1994 24 Hours Nürburgring and later competed in the FIA GT Championship and Italian GT Championship. As an investor, Wolff founded Marchfifteen in 1998 and Marchsixteen Investments in 2004, initially focusing on Internet and technology companies. He specializes in strategic investments in medium-sized industrial and listed companies, which have included Williams F1 and German HWA AG.</p>
                
                <p>For 2019, Mercedes continued its success by securing a sixth consecutive double world championship at the Japanese GP, when the team secured the Constructors' title and only one of Lewis Hamilton and Valtteri Bottas could still become Drivers' champion. This is an <strong>unprecedented achievement in the history of the sport.</strong> As of 2019, Wolff is <em>the only</em> Team Principal who has won more than five consecutive double world championships.</p>

                <p>In 2020, Mercedes secured a seventh consecutive double world championship. The championship set an all-time record of consecutive constructors' championships, ahead of the six Ferrari achieved from 1999 to 2004. In the same season, Hamilton became the most successful driver in terms of race wins at the 2020 Portuguese GP and secured a seventh world drivers' championship at the Turkish GP to tie the record held by Michael Schumacher.The team followed up the achievement with an eighth consecutive constructors' world championship in 2021, whilst finishing second in the drivers.</p>
                
                <p>Since the introduction of the turbo-hybrid regulations in 2014, Mercedes has won 112 of 182 races under Wolff's leadership. The team has taken 119 of 182 pole positions, 74 front-row lockouts, and 249 from 364 possible podium finishes. Since Wolff joined Mercedes in 2013, the team has achieved a winning percentage of 57%.</p>

                <p>After the 2020 F1 season, Wolff signed a new deal with Mercedes to continue as team principal and CEO for at least another three years. <strong>As of 2021, Wolff's net worth is estimated at CHF 1.25 billion (USD ~1.36 billion).</strong></p>  

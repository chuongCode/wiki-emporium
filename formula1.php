                    <h3>2014-2021</h3>

                    <p>Both drivers were retained for 2014. Rosberg won the first race of the season in Australia, then in Malaysia, <strong>Hamilton completed a grand slam </strong>– leading every 
                    lap from pole position, with the fastest race lap – while Rosberg completed a 1–2 finish for the team; it ended Hamilton's nine race streak without a podium finish, 
                    and was the first 1–2 finish by Mercedes as a works team since the 1955 Italian Grand Prix.</p>
                    <p>At the Russian Grand Prix the team won their first Constructors' Championship as a works team. Hamilton won the last race of the season, held in Abu Dhabi. 
                    He finished the season 67 points ahead of Rosberg, clinching the World Drivers' Championship. The team finished the 2014 season 296 points 
                    <em>ahead of their closest rival </em>Red Bull Racing in the World Constructors' Championship standings. With 18 pole positions, 
                    16 wins and 11 1–2s out of 19 races, Mercedes dominated this first year of the V6 turbo era. For their 16 race victories, 
                    the average winning margin to the nearest non-Mercedes competitor was 23.2 seconds.</p>
                    <p>At the 2015 Russian Grand Prix the team won their second Constructors' Championship as a works team and Hamilton won his second consecutive Drivers'  
                    Championship at the 2015 United States Grand Prix, finishing 59 points ahead of Rosberg. Mercedes continued their domination in this second year of the V6 
                    turbo era, improving on their impressive numbers from 2014 with 18 pole positions, 16 wins and 12 1–2s in 19 races. Of their 16 race victories, the average 
                    winning margin to the nearest non-Mercedes competitor was 19.7 seconds, down from 23.2 seconds in 2014.</p>
                    <p>In the 2016 season, Mercedes won the Constructors' Championship for the third consecutive season, winning 19 of the 21 races held, while securing 20 poles
                    (the highest percentage ever in a single season of F1 at 95.2%) and 8 1–2s. The average winning gap to the nearest non-Mercedes driver dropped
                    to 14.6 seconds. Rosberg won his only Drivers' Championship, finishing 5 points ahead of Hamilton, before announcing his retirement shortly after 
                    winning the title.</p>
                    <p>On 16 January 2017, Valtteri Bottas was announced as Rosberg's replacement for the 2017 season and British GP3 driver George Russell was incorporated into the junior team. 
                    On 22 October 2017, Mercedes won the Constructors' Championship for the fourth consecutive time. One week later, Lewis Hamilton became the first British driver to win four world championships. 
                    Mercedes finished the 2017 season with 12 wins out of 20 races, 15 poles, 4 1–2s and an average winning margin to the nearest non-Mercedes driver of 13.1 seconds.</p>
                    <p>For the 2018 season the team retained both Hamilton and Bottas. In the 2018 season, Mercedes won 11 races (all for Hamilton) out of 21 races, took 10 fastest laps 
                    (three for Hamilton and seven for Bottas), 13 pole positions (eleven for Hamilton and two for Bottas), and four 1–2 finishes. This made <strong>Mercedes only the second team in the history 
                    in F1 to achieve the feat of winning 5 drivers' and constructors' titles in a row</strong>, after Ferrari did so between 2000 and 2004.</p>
                    <p>For the 2019 season, the team again retained both Hamilton and Bottas. The team continued the strong performance through the first half of the season. By the summer break, 
                    Mercedes had won 10 out of 12 races with Hamilton taking 8 victories to Bottas's 2, securing 1–2 finishes at the first 5 races of the season. The team would go on to take both the 
                    drivers and constructors titles for the sixth consecutive year.</p>
                    <p>The team debuted their "Dual-Axis-Steering" system during pre-season testing for the 2020 season. This allows the driver to change the toe of the front wheels by pushing or 
                    pulling on the steering wheel. This allows the driver to optimise the car for better tyre warming on the straights with zero toe, or better mechanical grip in the corners with positive toe. 
                    The system was allowed for the 2020 season, but <strong>has been made illegal</strong> for 2021.</p>
                    <p>The team would go on to win both the drivers and constructors championships. Before the penultimate round of the season, the 2020 Sakhir Grand Prix, Hamilton was forced to sit the race out 
                    following a positive coronavirus test, with Williams driver and Mercedes junior George Russell replacing him.</p>
                    <p>For 2021, the team retained the same driver line-up for a fifth straight season. The team failed to win the drivers' championship for the first time in the V6 turbo-hybrid era, with Hamilton 
                    being overtaken by title rival Max Verstappen on the last lap of the season-ending Abu Dhabi Grand Prix, after <strong>a controversial safety car restart</strong>. Hamilton had comfortably led the race and been 
                    on course for his eighth title before a late safety car. </p>
                    <p>The actions of FIA race director Michael Masi surrounding the controversial procedures used during the late safety car caused Mercedes to file an intention to appeal the results of that race, though this was 
                    later withdrawn. Hamilton finished second in the championship, eight points behind champion Verstappen, with Bottas third in the standings in his final season with the team. <strong>The team 
                    took the constructors' title for a record-extending eighth consecutive season.</strong></p>


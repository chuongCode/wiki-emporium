                <h2>Toto Wolff (Source: Toto Wolff Wiki)</h2>
                <div>Team principal & CEO of the Mercedes-AMG Petronas F1 Team</div>

                <figure>
                <img src="images/totowolff.jpg" class="center-image" alt="Toto Wolff, the Mercedes Team Principle">
                <figcaption class="center-fig">Toto Wolff looking sharp!</figcaption>

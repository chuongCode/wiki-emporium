                        <p>Mercedes’ modern F1 revival started with the creation of a works squad for 2010 - the platform for a meteoric rise up the Grand Prix order. 
                        The team generated <em>huge</em> excitement from the off with the sensational return of Michael Schumacher, but headlines soon followed on track:  
                        three podiums in their debut season, all via Nico Rosberg - who then claimed a breakthrough pole/victory double at China in 2012.</p>
                        <p>The following season he was paired with Lewis Hamilton, the duo going on to stage some epic title battles as the Silver Arrows swept 
                        all before them to become one of the most dominant forces of the modern F1 era. And with Hamilton now partnered by the up-and-coming George 
                        Russell, Mercedes remain <em>very much</em> <strong>the team that everyone wants to beat...</strong></p>
                        <p>Mercedes-Benz, a brand of the Mercedes-Benz Group, has been involved in Formula One as both team owner and engine manufacturer for 
                        various periods since 1954. The Mercedes-AMG Petronas F1 Team, which is based in Brackley, England, and possesses a German licence, as of 2022 majority 
                        owned by the Mercedes-Benz Group with Toto Wolff having a <em>significant</em> shareholding. </p>
                        <p>Mercedes-branded teams are often referred to by the nickname, the "Silver Arrows". An announcement was made in December 2020 that Ineos planned to 
                        take a one third equal ownership stake alongside the Mercedes-Benz Group and Wolff; this came into effect on 25 January 2022.</p>
                        <p>Since a major rule shake-up in 2014, which required the use of turbochargers and hybrid electric engines, <strong> Mercedes has become one of the most 
                        successful teams in Formula One history, winning seven consecutive Drivers' titles from 2014 to 2020 and eight consecutive Constructors' 
                        titles from 2014 to 2021.</strong> It has set records for most wins in a season (19 in 2016), and for most consecutive constructors' titles. <strong>The 
                        manufacturer has also collected more than 200 wins as an engine supplier and is ranked second in Formula One history. Ten Constructors' and thirteen 
                        Drivers' Championships have been won with Mercedes-Benz engines.</strong></p>

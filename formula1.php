            <h2 class="full-width">Mercedes-AMG Petronas Formula One Team (2010–present) (Source: Mercedes-Benz Wiki)</h2>

                <section class="left">

                    <figure>
                        <img src="images/mercedes.jpg" class="center-image" alt="A Mercedes F1 car">

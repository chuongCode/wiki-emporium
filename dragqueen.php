				<h3> The Format </h3>
				<p> Prospective Drag Race contestants submit video auditions to the show's production company, World of Wonder. RuPaul, the host and head judge, views each tape and selects the season's competitors. Once the chosen pool of performers is on set, they film a series of episodes in which they compete against each other in various challenges. Each episode typically concludes with the removal of one contestant from the competition. Rarely, the outcome of an episode has been a double elimination, no elimination, contestant disqualification or removal of a contestant on medical grounds. Each episode features a so-called maxi challenge that tests competitors' skills in a variety of areas of drag performance. Some episodes also feature a mini challenge, the prize of which is often an advantage or benefit in the upcoming maxi challenge. Following the maxi challenge, contestants present themed looks in a runway walk.</p>

				<p> RuPaul and a panel of judges then critique each contestant's performance, deliberate amongst themselves, and announce the week's winner and bottom two competitors. The bottom two queens compete in a so-called Lip Sync for Your Life; the winner of the lip sync remains in the competition, and the loser is eliminated. The final three, or four depending on what RuPaul chooses, contestants remaining compete in a special finale episode wherein the season's winner is crowned. In early seasons, the finale was pre-recorded in the studio with no audience. More recently, the finale has taken the form of a lip sync tournament before a live audience. The season 12 finale was filmed remotely due to the COVID-19 pandemic. The whole season is typically filmed in four weeks.</p>


				<h2> Seasons 9–14 (2017–2022): The VH1 Era</h2>

				<h3> Seasons 9-14</h3>

				<img src="images/image4.png" alt="The promo image for Season 14">
				<p> <Strong>Season 9</Strong> began airing on March 24, 2017 on VH1, with cast members being announced on February 2, 2017. Visage returned as a main judge, while Kressley and Mathews returned as rotating main judges. This season features fourteen contestants competing for the title of "America's Next Drag Superstar", a one-year supply of Anastasia Beverly Hills cosmetics, and a cash prize of $100,000. This season featured the return of Cynthia Lee Fontaine, who previously participated in the season 8. Season 9 featured a top four in the finale episode, as opposed to the top three, which was previously established in season 4. The winner of season 9 was Sasha Velour, with Valentina winning Miss Congeniality.</p>
				<p><Strong>Season 10</Strong> began airing on March 22, 2018. Visage returned as a main judge, while Kressley and Mathews returned as rotating main judges. This season features thirteen new contestants, and one returning contestant, competing for the title of "America's Next Drag Superstar", a one-year supply of Anastasia Beverly Hills cosmetics, and a cash prize of $100,000. Eureka O'Hara, who was removed from the ninth season due to an injury, returned to the show after she accepted an open invitation. Season 10 premiered alongside the televised return of Untucked. The winner of season 10 was Aquaria, with Monét X Change winning Miss Congeniality.</p> 
				<p><Strong>Season 11</Strong> began airing February 28, 2019. This season had fifteen contestants, whereas previous seasons typically had fourteen contestants. Visage returned as a main judge, while Kressley and Mathews returned as rotating main judges. This season features fourteen new contestants, and one returning contestant, competing for the title of "America's Next Drag Superstar", a one-year supply of Anastasia Beverly Hills cosmetics, and a cash prize of $100,000. This season saw the return of Vanessa Vanjie Mateo, who was the first contestant eliminated in season 10. As with season 10, each week's episode was followed by an episode of the televised return of RuPaul's Drag Race: Untucked. The winner of season 11 was Yvie Oddly, with Nina West winning Miss Congeniality.</p>
				<p> On January 22, 2019, casting for <Strong>Season 12</Strong> was announced via YouTube and Twitter and was closed on March 1, 2019. On August 19, 2019, it was announced that the series had been renewed for a twelfth season. The season began airing on February 28, 2020. This is the first and only season to have the reunion and finale recorded virtually from the contestants' homes due to the COVID-19 pandemic. The winner of season 12 was Jaida Essence Hall, with Heidi N Closet winning Miss Congeniality.</p>
				<p> On December 2, 2019, casting for <Strong>Season 13 </Strong>was announced via YouTube and Twitter. The casting call closed on January 24, 2020. On August 20, 2020, it was announced the thirteenth season had been ordered by VH1. It began airing on January 1, 2021. The winner of season 13 was Symone, with Kandy Muse as runner-up, and LaLa Ri as Miss Congeniality.</p>
				<p> Casting for <Strong>Season 14</Strong> began on November 23, 2020. In August 2021, it was announced the fourteenth season had been ordered by VH1. The cast for the fourteenth season was revealed through VH1 on December 2, 2021. The season started airing on January 7, 2022. The winner of season 14 was Willow Pill, with Lady Camden as runner-up, and Kornbread "The Snack" Jeté as Miss Congeniality. The winning queen received a cash prize of $150,000, the highest amount awarded to date in a regular season. The runner-up received a $50,000 cash prize.</p>
				<h3> All Stars 7 and the Future of the Franchise</h3>
				<p> The cast of <Strong>RuPaul's Drag Race All Stars Season 7</Strong>, which had been airing on a yearly basis since the release of All Stars 2, was announced on April 13, 2022. It included eight winners of past seasons returning to compete, making it the first season featuring only past winners. It also became the first US season to feature a contestant from another franchise. The cast consisted of four winners of Drag Race: Jaida Essence Hall (season 12), Jinkx Monsoon (season 5), Raja (season 3) and Yvie Oddly (season 11); three winners of All Stars: Monét X Change and Trinity the Tuck (co-winning season 4) and Shea Couleé (season 5); and the winner of UK season 1, The Vivienne.  Jinkx Monsoon was crowned the Queen of All Queens, becoming the first to win two seasons of RuPaul's Drag Race. Monét X Change was the runner-up and Raja won a second title, "Queen of 'She Done Already Done Had Herses'." The release of this season marked the end of Drag Race, and it's spin-off shows airing on VH1, as All Stars was announced to be released through Paramount+, while the main series was announced to have been moved to MTV for the release of Season 15</p>

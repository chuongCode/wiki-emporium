				<section class="right">
					<h3> Seasons 5-8</h3>

					<img src="images/image3.png" alt="Winner of Season 8 Bob the Drag Queen posing with past winners">
					<p> <Strong>Season 5</Strong> began airing on January 28, 2013, with a 90-minute premiere episode. Fourteen contestants competed for the title of "America's Next Drag Superstar" along with a lifetime supply of Colorevolution Cosmetics, a one-of-a-kind trip courtesy of AlandChuck.travel, a headlining spot on Logo's Drag Race Tour featuring Absolut Vodka and a cash prize of $100,000. Rice and Visage were back as judges on the panel. The winner of season 5 was Jinkx Monsoon, with Ivy Winters winning Miss Congeniality.</p>

					<p><Strong>Season 6</Strong> began airing February 24, 2014. Like season 5, season 6 saw 14 contestants competing for the title of "America's Next Drag Superstar". For the first time in the show's history, the season premiere was split into two episodes; the fourteen queens are split into two groups and the seven queens in each group compete against one another before being united as one group in the third episode. Rice and Visage returned as judges at the panel. The winner won a prize package that included a supply from Colorevolution Cosmetics and a cash prize of $100,000. The winner of season 6 was Bianca Del Rio, with BenDeLaCreme winning Miss Congeniality.</p> 

					<p><Strong>Season 7</Strong> began airing on March 2, 2015. Returning judges included RuPaul and Visage, while the space previously occupied by Rice was filled by new additions Ross Mathews and Carson Kressley. Mathews and Kressley were both present for the season premiere and then took turns sharing judging responsibilities. Morales and Simon Sherry-Wood did not appear this season and were replaced by Bryce Eilenberg. Like the previous two seasons, this one featured fourteen contestants competing for the title of "America's Next Drag Superstar", a one-year supply of Anastasia Beverly Hills cosmetics, and a cash prize of $100,000. The winner of season 7 was Violet Chachki, with Katya winning Miss Congeniality.</p>

					<p> <Strong>Season 8</Strong> on began airing on March 7, 2016, with cast members announced during the NewNowNext Honors on February 1, 2016. Visage returned as a main judge, while Kressley and Mathews returned as rotating main judges. The first episode celebrated the 100th taping of the show, and the 100th drag queen to compete. Similar to season 2, this season had twelve contestants competing for the title of "America's Next Drag Superstar", a one-year supply of Anastasia Beverly Hills cosmetics, and a cash prize of $100,000. The winner of season 8 was Bob the Drag Queen, with Cynthia Lee Fontaine winning Miss Congeniality.</p>

					<p> To mark the end of RuPaul's Drag Race's time airing on Logo, <Strong>Rupaul's Drag Race: All Stars Season 2</Strong> began airing on August 25, 2016. The cast consisted of 10 returning contestants, Adore Delano, Alaska, Alyssa Edwards, Coco Montrese, Detox, Ginger Minj, Katya, Phi Phi O'Hara, Roxxxy Andrews, and Tatianna. A new twist was revealed for this season changing the format of the show, where the two best performing queens "Lip Sync for their Legacy", with the winner earning $10,000 and choosing one of the bottom queens to eliminate. The winner was Alaska, while Detox and Katya placed as the runners-up.</p>

				<section class = "left">
					<h3> Seasons 1-4</h3>

					<img src="images/image2.png" alt="The contestants of Season 1">
					<p> <Strong>Season 1 </Strong>premiered in the U.S. on February 2, 2009, on Logo TV. Nine contestants competed to become "America's Next Drag Superstar". The winner won a lifetime supply of MAC Cosmetics, was featured on the cover of Paper and in an LA Eyeworks campaign, joined the Absolut Pride tour, and won a cash prize of $20,000. One of the nine, Nina Flowers, was determined by an audience vote via the show's official website. The winner of season 1 was BeBe Zahara Benet, with Nina Flowers winning Miss Congeniality. In late 2013, Logo re-aired the season as RuPaul's Drag Race: The Lost Season Ru-Vealed, featuring commentary from RuPaul.</p>

					<p>For <Strong>season 2</Strong>, 12 contestants competed for a lifetime supply of NYX Cosmetics and be the face of nyxcosmetics.com, an exclusive one year public relations contract with LGBT firm Project Publicity, be featured in an LA Eyeworks campaign, join the Logo Drag Race Tour, and a cash prize of $25,000. A new tradition of writing a farewell message in lipstick on the workstation mirror was started by the first eliminated queen, Shangela. Each week's episode is followed by a behind-the-scenes show, RuPaul's Drag Race Untucked. The winner of season 2 was Tyra Sanchez, with Pandora Boxx winning Miss Congeniality. </p> 

					<p><Strong>Season 3</Strong> had Michelle Visage replacing Merle Ginsberg on the judging panel as well as Billy Brasfield , Mike Ruiz, and Jeffrey Moran (courtesy of Absolut Vodka) filling in for Santino Rice's absence during several episodes. Due to Billy B's continued appearances, he and Rice are considered to have been alternate judges for the same seat on judges panel. Other changes made included the introduction of a wildcard contestant from the past season, Shangela; an episode with no elimination; and a contestant, Carmen Carrera, being brought back into the competition after having been eliminated a few episodes prior. A new pit crew was also introduced consisting of Jason Carter and Shawn Morales. As with the previous season, each week's episode was followed by a behind-the-scenes show, RuPaul's Drag Race: Untucked. The winner of the season 3 was Raja, with Yara Sofia winning Miss Congeniality. </p>

					<p> <Strong>Season 4</Strong> began airing on January 30, 2012, with cast members announced November 13, 2011. The winner headlined Logo's Drag Race Tour featuring Absolut Vodka, won a one-of-a-kind trip, a lifetime supply of NYX Cosmetics, a cash prize of $100,000, and the title of "America's Next Drag Superstar". Like the previous season, Rice and Billy B, shared the same seat at the judges table alternatively, with Brasfield filling in for Rice when needed.The winner of season 4 was Sharon Needles, with Latrice Royale winning Miss Congeniality.</p>

					<p> The end of the first major era of RuPaul's Drag Race was marked with the release of <Strong>RuPaul's Drag Race: All Stars</Strong> on October 22nd, 2012, and featured a full cast of returning queens from previous seasons as voted by the show's fans. The queens that were invited back to compete were: Alexis Mateo, Chad Michaels, Jujubee, Latrice Royale, Manila Luzon, Mimi Imfurst, Nina Flowers, Pandora Boxx, Raven, Shannel, Tammie Brown and Yara Sofia. It is the only season of All Stars to have the contestants compete in teams of two. The winner received a supply of MAC cosmetics, a "one of a kind trip" and $100,000. The winner was Chad Michaels, while Raven placed as the runner-up, again.</p>


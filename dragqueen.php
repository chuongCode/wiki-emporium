			<h2 class="center"> About Drag Race </h2>

			<img src="images/image0.png" class="center-image" alt="The marketing image for Season 15">

			<p> RuPaul's Drag Race is an American reality competition television series, the <em>first</em> in the Drag Race franchise, produced by World of Wonder for Logo TV (season 1–8), WOW Presents Plus, VH1 (season 9–14) and, beginning with the fifteenth season, MTV. The show documents RuPaul in the search for "America's next drag superstar" RuPaul plays the role of host, mentor, and head judge for this series, as contestants are given different challenges each week. Contestants are judged by a panel that includes RuPaul, Michelle Visage, an alternating third main judge of either Carson Kressley, Ross Mathews, or Ts Madison, and one or more guest judges, who critique their progress throughout the competition. The title of the show is a play on drag queen and drag racing, and the title sequence and song "Drag Race" both have a drag-racing theme. </p>
